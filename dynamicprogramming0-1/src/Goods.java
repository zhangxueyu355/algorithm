
public class Goods {
	private int weight;
	private int value;
	public Goods(int weight,int value){
		this.weight = weight;
		this.value = value;
	}
	
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}

}


